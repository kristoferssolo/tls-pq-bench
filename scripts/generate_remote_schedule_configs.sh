#!/usr/bin/env bash
set -euo pipefail

script_dir="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd)"
repo_dir="$(cd -- "${script_dir}/.." && pwd)"

if [[ -n "${SCHEDULE_ENV_FILE:-}" ]]; then
    # shellcheck disable=SC1090
    source "${SCHEDULE_ENV_FILE}"
fi

: "${SERVER_HOST:?set SERVER_HOST to the remote benchmark server IP or hostname}"
: "${SERVER_NAME:?set SERVER_NAME to the TLS DNS name or IP to verify}"

CA_CERT="${CA_CERT:-certs/ca.der}"
TRACK_CONFIG="${TRACK_CONFIG:-${repo_dir}/benchmarks/remote-recurring.toml}"
FULL_CONFIG="${FULL_CONFIG:-${repo_dir}/benchmarks/remote-full.toml}"
TRACK_ITERS="${TRACK_ITERS:-200}"
TRACK_WARMUP="${TRACK_WARMUP:-20}"
FULL_ITERS="${FULL_ITERS:-200}"
FULL_WARMUP="${FULL_WARMUP:-20}"

mode_ports=$'x25519 raw 4433\nx25519 http1 4434\nsecp256r1 raw 4435\nsecp256r1 http1 4436\nx25519mlkem768 raw 4437\nx25519mlkem768 http1 4438\nsecp256r1mlkem768 raw 4439\nsecp256r1mlkem768 http1 4440'

emit_header() {
    local out_path="$1"
    local purpose="$2"
    cat >"${out_path}" <<EOF
# Generated by scripts/generate_remote_schedule_configs.sh
#
# ${purpose}
# Remote server host: ${SERVER_HOST}
# Verified server name: ${SERVER_NAME}
# CA certificate path on runner host: ${CA_CERT}

EOF
}

emit_entry() {
    local out_path="$1"
    local mode="$2"
    local proto="$3"
    local port="$4"
    local payload="$5"
    local concurrency="$6"
    local iters="$7"
    local warmup="$8"

    cat >>"${out_path}" <<EOF
[[benchmarks]]
server = "${SERVER_HOST}:${port}"
server_name = "${SERVER_NAME}"
proto = "${proto}"
mode = "${mode}"
verification = { kind = "cacert", path = "${CA_CERT}" }
payload = ${payload}
iters = ${iters}
warmup = ${warmup}
concurrency = ${concurrency}

EOF
}

generate_tracking_config() {
    emit_header "${TRACK_CONFIG}" "Reduced recurring matrix for the every-4-hours timer."

    while read -r mode proto port; do
        for payload in 1024 1048576; do
            for concurrency in 1 100; do
                emit_entry \
                    "${TRACK_CONFIG}" \
                    "${mode}" \
                    "${proto}" \
                    "${port}" \
                    "${payload}" \
                    "${concurrency}" \
                    "${TRACK_ITERS}" \
                    "${TRACK_WARMUP}"
            done
        done
    done <<<"${mode_ports}"
}

generate_full_config() {
    emit_header "${FULL_CONFIG}" "Full daily matrix covering all modes, protocols, payloads, and concurrencies."

    while read -r mode proto port; do
        for payload in 1024 10240 102400 1048576; do
            for concurrency in 1 10 100; do
                emit_entry \
                    "${FULL_CONFIG}" \
                    "${mode}" \
                    "${proto}" \
                    "${port}" \
                    "${payload}" \
                    "${concurrency}" \
                    "${FULL_ITERS}" \
                    "${FULL_WARMUP}"
            done
        done
    done <<<"${mode_ports}"
}

mkdir -p "$(dirname -- "${TRACK_CONFIG}")" "$(dirname -- "${FULL_CONFIG}")"

generate_tracking_config
generate_full_config

printf 'wrote %s\n' "${TRACK_CONFIG}"
printf 'wrote %s\n' "${FULL_CONFIG}"
